#String index()

mi_texto = "Esto es una prueba"
resultado = mi_texto[0]
print(resultado)

resultado = mi_texto.index("a")
print(resultado)

resultado = mi_texto.index("prueba")
print(resultado)

#SubString

texto = "ABCDEFGHIJKLM"
fragmento = texto[0:3]
print(fragmento)

fragmento = texto[2:]
print(fragmento)

fragmento = texto[:3]
print(fragmento)

fragmento = texto[:]
print(fragmento)

fragmento = texto[::2]
print(fragmento)

fragmento = texto[::2]
print(fragmento)